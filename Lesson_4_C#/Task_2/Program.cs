void PrintImage(int[,] image) //рисуем картинку
{
 for (int i = 0; i < image.GetLength(0); i++)
 {
 for (int j = 0; j < image.GetLength(1); j++)
 {
 if(image[i,j] == 0) Console.Write($" "); //проверка текущего элемента на совпадение с 0, то печатается пробел 
 else Console.Write($"="); //если 1 = печатается =
 }
 Console.WriteLine();
 }
}

void FillImage(int row, int col) //заполняем картинку 
{
 if (pic[row, col] == 0) //pic - пиксель, если пиксель = 0, то
 {
 pic[row, col] = 1; 
 FillImage(row - 1, col); // строчка выше 
 FillImage(row, col - 1); // строчка слева
 FillImage(row + 1, col); // строчка ниже
 FillImage(row, col + 1); // строка вправа
 }
}
